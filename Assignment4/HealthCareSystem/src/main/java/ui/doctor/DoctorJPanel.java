    private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblPerson1.getModel();
        model.setRowCount(0);
        
        for (Person p : personDirectory.getPersonDirectory()) {
            if(p.getPatient().getAppointmentDate() != null) {
                Object row[] = new Object[5];
                row[0] = p.getPatient();
                row[1] = p.getFirstName() + " " + p.getLastName();
                row[2] = (p.getPatient().getAppointmentDate().getMonth() + 1) + "-" + (p.getPatient().getAppointmentDate().getDate()) + "-" + (p.getPatient().getAppointmentDate().getYear() + 1900);
                row[3] = "Pending";
                row[4] = p.getPatient().isIsNormal() ? "No" : "Yes";
                model.addRow(row);
            }

        }
        

}
