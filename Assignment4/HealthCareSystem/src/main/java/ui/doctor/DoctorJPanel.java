    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblPerson1.getSelectedRow();

        
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(tblPerson1.getValueAt(selectedRowIndex, 3).equals("Completed")){
            JOptionPane.showMessageDialog(null, "The task is already completed", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else if(txtBlood.getText().isEmpty() || txtHeartRate.getText().isEmpty()|| txtTemperature.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "All Enter value for all fields!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }else {
            float bloodPressure;
            Integer heartRate;
            float temperature;
            try {
                bloodPressure = Float.parseFloat(txtBlood.getText());
                heartRate = Integer.parseInt(txtHeartRate.getText());
                temperature = Float.parseFloat(txtTemperature.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Please Enter numeric value", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Patient p = (Patient) tblPerson1.getValueAt(selectedRowIndex, 0);
            Encounter encounter = p.getEncounterHistory().addEncounter();
            encounter.setDate(p.getAppointmentDate());
            VitalSigns vitalSigns = encounter.createVitalSign();
            vitalSigns.setBloodPressure(bloodPressure);
            vitalSigns.setHeartRate(heartRate);
            vitalSigns.setTemperature(temperature);
            vitalSigns.isPatientAbnormal(p);
            p.setAppointmentDate(null);
            tblPerson1.setValueAt("Completed", selectedRowIndex, 3);
            tblPerson1.setValueAt(p.isIsNormal() ? "No" : "Yes", selectedRowIndex, 4);
            JOptionPane.showMessageDialog(this, "Successfully Added", "Info", JOptionPane.INFORMATION_MESSAGE);
            txtBlood.setText("");
            txtHeartRate.setText("");
            txtTemperature.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


